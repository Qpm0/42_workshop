#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char		*str;
	size_t		strsize;

	if (!s)
		return (NULL);
	strsize = ft_strlen(s);
	if (start >= strsize)
		start = strsize;
	if (len > strsize - start)
		len = strsize - start;
	str = (char *)ft_calloc(1, sizeof(char) * len + 1);
	if (!str)
		return (NULL);
	if (s[start])
		ft_strlcpy(str, &s[start], len + 1);
	return (str);
}
